/**
 * Helper to dynamically translate dynamic destination object fields based on chosen language
 */
export function getTranslatedDestination(dest: Destination, lang: 'en' | 'ta' | 'hi'): Destination {
  if (lang === 'en') return dest;
  const trans = DESTINATION_TRANSLATIONS[lang]?.[dest.id];
  if (!trans) return dest;

  const resolvedTimeline = dest.historyHub.timeline.map((item, idx) => {
    const tItem = trans.historyHub?.timeline?.[idx];
    return tItem ? { ...item, ...tItem } : item;
  });

  return {
    ...dest,
    name: trans.name || dest.name,
    description: trans.description || dest.description,
    district: trans.district || dest.district,
    category: trans.category || dest.category,
    budgetLevel: trans.budgetLevel || dest.budgetLevel,
    travelStyle: trans.travelStyle || dest.travelStyle,
    travelDuration: trans.travelDuration || dest.travelDuration,
    entryFees: trans.entryFees || dest.entryFees,
    timings: trans.timings || dest.timings,
    bestSeason: trans.bestSeason || dest.bestSeason,
    bestVisitingHours: trans.bestVisitingHours || dest.bestVisitingHours,
    historyHub: {
      ...dest.historyHub,
      history: trans.historyHub?.history || dest.historyHub.history,
      whyFamous: trans.historyHub?.whyFamous || dest.historyHub.whyFamous,
      architecture: trans.historyHub?.architecture || dest.historyHub.architecture,
      dynasty: trans.historyHub?.dynasty || dest.historyHub.dynasty,
      culturalImportance: trans.historyHub?.culturalImportance || dest.historyHub.culturalImportance,
      timeline: resolvedTimeline
    },
    storyMode: {
      ...dest.storyMode,
      legend: trans.storyMode?.legend || dest.storyMode.legend,
      historicalStory: trans.storyMode?.historicalStory || dest.storyMode.historicalStory,
      importantFacts: trans.storyMode?.importantFacts || dest.storyMode.importantFacts
    }
  };
}
